    private void tablaDeClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaDeClientesMouseClicked
        int i = tablaDeClientes.getSelectedRow();
        if (i != -1) {
            DataCliente c = (DataCliente) dataClientes[i][2];

            nicknameCliente.setText(c.getNickname());
            apellidoCliente.setText(c.getApellido());
            direccionCliente.setText(c.getDireccion().getCalle()+" "+c.getDireccion().getNumero_puerta());
            mailCliente.setText(c.getMail());
            nombreCliente.setText(c.getNombre());

            String img = c.getRutaImagen();

            if (img == null) {
                    //Image image = Toolkit.getDefaultToolkit().createImage(IMAGEN POR DEFECTO);
                //Icon warnIcon = new ImageIcon(image);
                // imagenCliente.setIcon(warnIcon);
                //imagenCliente.validate();
            } else {

                Image image = Toolkit.getDefaultToolkit().createImage(img);
                Icon warnIcon = new ImageIcon(image.getScaledInstance(imagenCliente.getWidth(), imagenCliente.getHeight(), image.SCALE_DEFAULT));
                imagenCliente.setIcon(warnIcon);
                imagenCliente.validate();

            }
            
            //PEDIDOS
            
            int k = pedidosClienteModel.getRowCount();
             for (int j = k-1; j >=0 ; j--) {
                    pedidosClienteModel.removeRow(j);    
             }
             
            
            ArrayList<DataPedido> elPedido = iUsr.listarPedidosCliente(c.getNickname());

            
            for (DataPedido pedido : elPedido) {
            {
                elPedidoCliente.put(pedidosClienteModel.getRowCount(), pedido);
                String[] fila1 = new String[4];
                fila1[0] = (Integer.toString(pedido.getNumero()));
                Calendar FechaPedido = pedido.getFechaPedido();
                int diaHoy = FechaPedido.get(Calendar.DAY_OF_MONTH);
                int mes=FechaPedido.get(Calendar.MONTH);
                int año =FechaPedido.get(Calendar.YEAR);
                fila1[2] = (diaHoy)+"/"+(mes)+"/"+(año);
                fila1[1] = pedido.getNickNameRestaurante();
                pedidosClienteModel.addRow(fila1);


            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_tablaDeClientesMouseClicked
  } 

